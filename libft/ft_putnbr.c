#include "ft_printf.h"

void	ft_putnbr(int n, int *n_i)
{
	if (n == -2147483648)
	{
		*n_i += write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		*n_i += write(1, "-", 1);
		n *= -1;
	}
	if (n <= 9)
	{
		ft_putchar(n + '0', n_i);
	}
	if (n > 9)
	{
		ft_putnbr(n / 10, n_i);
		ft_putnbr(n % 10, n_i);
	}
}
