#include "ft_printf.h"

void	ft_putstr(char *s, int *n_i)
{
	size_t	len;

	if (!s)
	{
		*n_i += write(1, "(null)", 6);
	}
	else
	{
		len = ft_strlen(s);
		*n_i += write(1, s, len);
	}
}
